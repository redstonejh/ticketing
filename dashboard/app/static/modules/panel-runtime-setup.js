export const initializePanelRuntimes = ({
  dashboardPanelRuntime,
  dashboardPanelContainment,
  DASHBOARD_GRID_COLUMNS,
  DASHBOARD_GRID_ROW_HEIGHT,
  workspaceObjectCapabilities,
  gridRowsFromHeight,
  gridHeightForRows,
  gridGapForLayout,
  gridItemRowSpan,
  gridItemMinimumSpan,
  createGridMetrics,
  gridBoundsForItem,
  panelMinimumRows,
  openPanelForInternalDrop,
  applyVerticalPanelExpansion,
  resolveSparseGridLayout,
  reflowItemsForLayout,
  undoTransientItemClasses,
  groupItemLayoutKey,
  applyWidgetSpan,
  applyWidgetGridPosition,
  applyPanelColor,
  gridCellFromPoint,
  initWidgetLayout,
  readDraftList,
  writeDraftList,
  gridItemLayoutKey,
  regionIdForWorkspaceItem,
  emitWorkspaceEvent,
  commitActiveDropSlot,
  viewportRowFloorForLayout,
  cleanupWidgetRowBreaks,
}) => {
  const panelRuntime = dashboardPanelRuntime.createRuntime({
    columns: DASHBOARD_GRID_COLUMNS,
    workspaceObjectCapabilities,
    gridRowsFromHeight,
    gridHeightForRows,
    gridGapForLayout,
    gridItemRowSpan,
    gridItemMinimumSpan,
  });

  const panelContainmentRuntime = dashboardPanelContainment.createRuntime({
    columns: DASHBOARD_GRID_COLUMNS,
    rowHeight: DASHBOARD_GRID_ROW_HEIGHT,
    workspaceObjectCapabilities,
    createGridMetrics,
    gridBoundsForItem,
    gridHeightForRows,
    gridRowsFromHeight,
    gridGapForLayout,
    gridItemRowSpan,
    gridItemMinimumSpan,
    getPanelMinimumHeight: panelRuntime.getPanelMinimumHeight,
    panelMinimumRows,
    applyPanelHeight: panelRuntime.applyPanelHeight,
    applyPanelSpan: panelRuntime.applyPanelSpan,
    openPanelForInternalDrop,
    applyVerticalPanelExpansion,
    resolveSparseGridLayout,
    reflowItemsForLayout,
    undoTransientItemClasses,
    groupItemLayoutKey,
    applyWidgetSpan,
    applyWidgetGridPosition,
    applyPanelColor,
    gridCellFromPoint,
    initWidgetLayout,
    readDraftList,
    writeDraftList,
    gridItemLayoutKey,
    regionIdForWorkspaceItem,
    emitWorkspaceEvent,
    commitActiveDropSlot,
    viewportRowFloorForLayout,
    cleanupWidgetRowBreaks,
  });

  return {
    panelRuntime,
    panelContainmentRuntime,
  };
};
